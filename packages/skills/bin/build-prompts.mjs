#!/usr/bin/env node
/**
 * build-prompts — project the editable prompt markdown into an embedded TS module.
 *
 * The system prompts that steer each generation layer live as markdown in packages/skills/prompts/*.md
 * (the source of truth — edit them freely, they are just text). This script embeds them into
 * src/prompts.generated.ts so the skills package stays isomorphic (no fs at runtime, golden invariant
 * #4) and needs no build step. Same "text is truth; the projection is derived" stance as the product.
 *
 *   npm run prompts:build       # after editing any prompts/*.md
 */
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const pkg = join(here, "..");
const promptsDir = join(pkg, "prompts");

/** Strip leading YAML frontmatter (--- … ---); the prompt is everything after it. */
function body(md) {
  const m = md.match(/^---\n[\s\S]*?\n---\n?/);
  return (m ? md.slice(m[0].length) : md).trim();
}

const files = readdirSync(promptsDir).filter((f) => f.endsWith(".md")).sort();
const prompts = {};
for (const f of files) prompts[f.replace(/\.md$/, "")] = body(readFileSync(join(promptsDir, f), "utf8"));

const out = [
  "// GENERATED by bin/build-prompts.mjs from prompts/*.md — DO NOT EDIT.",
  "// Edit the markdown in packages/skills/prompts/, then run `npm run prompts:build`.",
  "// The .md files are the source of truth; this embed keeps the package isomorphic (no fs at runtime).",
  "",
  `export const PROMPTS: Record<string, string> = ${JSON.stringify(prompts, null, 2)};`,
  "",
].join("\n");

writeFileSync(join(pkg, "src", "prompts.generated.ts"), out);
console.log(`Embedded ${files.length} prompts → src/prompts.generated.ts`);
for (const k of Object.keys(prompts)) console.log(`  ${k} (${prompts[k].length} chars)`);
