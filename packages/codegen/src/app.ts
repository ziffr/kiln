function architectureDoc(m: AppModel): string {
  const list = (xs: string[]) => (xs.length ? xs.map((x) => `- ${x}`).join("\n") : "- (none)");
  return `# Architecture — ${m.domain}

Generated from a VerticalBusinessDesigner model. This documents **what** each part is, **why** it exists, and the **decisions** baked in.

## Overview
A two-tier app: a zero-dependency Node API (\`server.mjs\`) over an in-memory store, and a Vite/React admin (\`web/\`). Both are driven by the same model (\`model.json\`).

## Domain model (entities)
${list(m.entities.map((e) => `**${e.name}** (\`${e.id}\`, area: ${e.area}) — fields: ${e.fields.map((f) => `${f.name}:${f.type}`).join(", ") || "none"}${e.references.length ? `; references ${e.references.join(", ")}` : ""}`))}

## Behaviour (commands → events)
${list(m.commands.map((c) => `**${c.name}** on \`${c.entity}\`${c.emits.length ? ` → emits ${c.emits.join(", ")}` : ""}`))}

## Automations (reactions)
${list(m.policies.map((p) => `on \`${p.on}\` → run \`${p.then}\``))}

## Roles & access
${m.roles.length ? list(m.roles.map((r) => `**${r.name}** — operates ${r.capabilities.join(", ") || "(none)"}`)) : "- No roles modelled — writes are open. Add roles in the designer to gate them."}
Write access is enforced in \`server.mjs\` via the \`x-role\` header against \`PERMISSIONS\` (a scaffold — replace with real authentication).

## Business areas
${list(m.areas.map((a) => `**${a.name}** — ${a.capabilities.join(", ")}`))}

## Key decisions
- **SQLite via built-in \`node:sqlite\`** — real persistence (\`data.db\`) with zero npm dependencies; requires Node ≥ 22. Swap for Postgres when you need concurrency/scale.
- **Command endpoints** (not just CRUD) so business actions and their events/automations are first-class.
- **Typed-field validation** and **role-gated writes** are enforced server-side; the client mirrors the field types.
- **Handlers isolated** in \`handlers.mjs\` so business logic can evolve (or be regenerated) without touching transport.

## Security notes (before production)
- Set \`CORS_ORIGIN\` to your web origin (defaults to \`*\`).
- Replace the \`x-role\` scaffold with real authentication + session management.
- Move the store to a database and add persistence, migrations and backups.
`;
}

function readme(m: AppModel): string {
  return `# ${m.domain} — generated application

A runnable full-stack starter derived from the business model (${m.entities.length} entities, ${m.commands.length} commands, ${m.events.length} events, ${m.policies.length} automations).

## Run it

**API** (no install needed — zero dependencies; requires **Node ≥ 22** for built-in SQLite):
\`\`\`
npm start        # http://localhost:8787  (persists to data.db)
\`\`\`

**Admin client:**
\`\`\`
cd web && npm install && npm run dev    # http://localhost:5173 (proxies /api to the server)
\`\`\`

## What's here
- \`server.mjs\` — REST per entity (typed-field validation, role-gated writes), a POST endpoint per command (record + events + automations), an event log. **SQLite** persistence (\`data.db\`, via built-in \`node:sqlite\`).
- \`handlers.mjs\` — the business logic per command (isolated so it can be refined/regenerated).
- \`web/\` — a React admin: a role picker, a screen per entity (typed form + command buttons) grouped by business area, and a live event log.
- \`ARCHITECTURE.md\` — what/why/decisions + security notes. \`model.json\` — the source model.
- \`eslint.config.js\`, \`.prettierrc\`, \`.editorconfig\`, \`jsconfig.json\` — lint/format/editor baseline.

## Quality
\`\`\`
npm run format     # prettier
npm run lint       # eslint (run: npm i, then npm run lint)
\`\`\`

## Security (before production)
- Writes are gated by role via the \`x-role\` header (a **scaffold** — replace with real auth). Set \`AUTH=off\` to disable, \`CORS_ORIGIN\` to your web origin.
- Swap the in-memory store for a database.

This is a starting point to refine — the model wires the structure; the handler + screen bodies are yours (or the AI-logic export's) to flesh out.
`;
}
